// A loop is a control structure that allows you to repeatedly execute a block of code until a specific condition is met. The key components of a loop are:

// Initialization: Setting up any variables needed for the loop.
// Condition: The expression that is evaluated before each iteration. If the condition evaluates to true, the loop continues; otherwise, it terminates.
// Iteration/Update: The process of changing the loop variables in a way that eventually causes the condition to become false.
// Body: The block of code that is executed in each iteration.
// There are three types:-
// 1. for loop 
// 2. while loop
// 3. do-while loop